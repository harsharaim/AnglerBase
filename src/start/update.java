    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
     JFrame frame = new JFrame("Update");
       if(JOptionPane.showConfirmDialog(frame,"Confirm if you want Update", "Update",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_NO_OPTION)
       {  
            String fname=f_name.getText();
            String lname=l_name.getText();
            String mobnum=mob.getText();
            String email=mail.getText();
            String bdate=dob.getText();
            String gender=gen.getText();
            String state=ustate.getText();
            String place=uplace.getText();
            PreparedStatement ps;
             try {
                 String query="Update user set fname=?,lname=?,phone=?,email=?,DOB=?,state=?,gender=?,place=? WHERE uname =?";
                 ps=DBConnection.getCon("jdbc:mysql://localhost:3306/miniproject", "root", "YourPassword").prepareStatement(query);
                 ps.setString(1,fname);
                 ps.setString(2,lname);
                 ps.setString(3,mobnum);
                 ps.setString(4,email);
                 ps.setString(5,bdate);
                 ps.setString(6,state);
                 ps.setString(7,gender);
                 ps.setString(8,place);
                 ps.setString(9,upid);
                 ps.executeUpdate();
                 if(ps.executeUpdate()>0){
                    JOptionPane.showMessageDialog(null, "Update success");
                 }else{
                    JOptionPane.showMessageDialog(null, "Failed to update the value.","Error",JOptionPane.ERROR_MESSAGE);
                 }
              }catch (Exception ex) {
                 
                 JOptionPane.showMessageDialog(null, "Invalid values OR input field is empty","Error",JOptionPane.ERROR_MESSAGE);
                // Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
              }
        }
    }//GEN-LAST:event_submitActionPerformed
