    private void submit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit1ActionPerformed
        upid=fisherid.getText();
        if(upid.isEmpty()){
           JOptionPane.showMessageDialog(this,"Enter a fishermen id.","Empty Field",JOptionPane.ERROR_MESSAGE);
        }
        else{
          try{  
             Connection con = DBConnection.getCon("jdbc:mysql://localhost:3306/miniproject", "root", "YourPassword");
             String login = "select * from user where uname=?";
             PreparedStatement stmt = con.prepareStatement(login);
             stmt.setString(1, upid);
             ResultSet rs = stmt.executeQuery(); 
            if (rs.next())
             {
               f_name.setText(rs.getString(3));
               l_name.setText(rs.getString(4));
               mob.setText(rs.getString(9));  
               mail.setText(rs.getString(8));
               dob.setText(rs.getString(5));
               gen.setText(rs.getString(10));
               ustate.setText(rs.getString(6));
               uplace.setText(rs.getString(7)); 
              }
             else
              {
               JOptionPane.showMessageDialog(rootPane, "Invalid Fishermen id","Error",JOptionPane.ERROR_MESSAGE);       
              }
            }catch(java.sql.SQLException ex) 
             {
                //Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                System.out.println("error");
             }
           }
    }//GEN-LAST:event_submit1ActionPerformed
